    //đổ csdl menu vào list menu bằng cách add panel
    public void fillMenu(){
        pnlKhaivi.removeAll();
        pnlSushi.removeAll();
        pnlTempura.removeAll();
        pnlDiemtam.removeAll();
        pnlTrangMieng.removeAll();
        try{
            List<SanPham> spl = spd.selectAll();
            for(SanPham sp : spl){
                MenuPanelChild mnpnl = new MenuPanelChild(sp);
                mnpnl.setSize(200, 190);
                if(sp.getMaloai()==1)
                    pnlKhaivi.add(mnpnl);
                else if(sp.getMaloai()==2)
                    pnlSushi.add(mnpnl);
                else if(sp.getMaloai()==3)
                    pnlDiemtam.add(mnpnl);
                else if(sp.getMaloai()==4)
                    pnlTempura.add(mnpnl);
                else
                    pnlTrangMieng.add(mnpnl);
            }
        }
        catch(Exception ex){
            //MsgBox.alert(this, "Lỗi kết nối dữ liệu");
            throw new RuntimeException(ex);
        }
    }
    
    //setForm
    public static void setForm(SanPham sp){
        txtTenmon.setText(sp.getTenSp());
        txtGia.setText(String.valueOf(sp.getDonGia()));     
        txtMota.setText(sp.getMota());
        
        if(sp.getMaloai()==1)                                                   //set loại
            cboLoai.setSelectedIndex(0);
        else if(sp.getMaloai()==2)
            cboLoai.setSelectedIndex(1);
        else if(sp.getMaloai()==3)
            cboLoai.setSelectedIndex(2);
        else if(sp.getMaloai()==4)
            cboLoai.setSelectedIndex(3);
        else
            cboLoai.setSelectedIndex(4);
        
        if(sp.getTinhTrang())                                                   //set tình trạng
            rdoCon.setSelected(true);
        else
            rdoHet.setSelected(true);
        
        loadImg(sp.getHinh());
        updateStatus();
    }    
    
    //getForm
    public SanPham getForm(){
        SanPham sp = new SanPham();
        sp.getHinh();
        sp.setTenSp(txtTenmon.getText());
        sp.setDonGia(Double.valueOf(txtGia.getText().toString()));
        
        if(cboLoai.getSelectedIndex()==0)
            sp.setMaloai(1);
        else if(cboLoai.getSelectedIndex()==1)
            sp.setMaloai(2);
        else if(cboLoai.getSelectedIndex()==2)
            sp.setMaloai(3);
        else if(cboLoai.getSelectedIndex()==3)
            sp.setMaloai(4);
        else
            sp.setMaloai(5);
        
        if(rdoCon.isSelected())
            sp.setTinhTrang(true);
        else 
            sp.setTinhTrang(false);
        
        sp.setMota(txtMota.getText());
        sp.setHinh(lblHinh.getToolTipText());
        return sp;
    }
    
    
    //clearForm
    public void clearForm(){        
        txtTenmon.setText("");
        txtGia.setText("00");
        rdoCon.setSelected(true);
        txtMota.setText("");
        cboLoai.setSelectedIndex(0);
        lblHinh.setIcon(null);
        lblHinh.setToolTipText(null);
        
        Auth.sanpham = null;
        updateStatus();
    }
    
    //load img
    public static void loadImg(String name){
        lblHinh.setSize(197, 138);
        ImageIcon avata = XImage.readImg(name, lblHinh);
        lblHinh.setIcon(avata);
        lblHinh.setToolTipText(name);
    }
    
    //chọn và đọc img
    public void getImg(){
        JFileChooser fChooser = new JFileChooser();
        if(fChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            File file = fChooser.getSelectedFile();
            XImage.saveImg(file);                                               //lưu file
            loadImg(file.getName());
        }
    }
    
    
