    //check validate
    public boolean checkValidate(){
        try{
            Double gia = Double.valueOf(txtGia.getText().toString().trim());
            if(txtTenmon.getText().length()== 0){
                MsgBox.alert(this, "Vui lòng nhập tên sp");
                return false;
            }
            else if(txtGia.getText().length()==0 || gia < 20000){
                MsgBox.alert(this, "Vui lòng nhập giá món ăn > 20k ");
                return false;
            }
            else if(txtMota.getText().length()==0){
                MsgBox.alert(this, "Vui lòng nhập mô tả cho sản phẩm");
                return false;
            }
            else
                return true;
        }
        catch(Exception e){
            MsgBox.alert(this, "Giá món ăn phải thể hiện bằng số");
            return false;
        }
        
    }
    
    //check tên trùng ( mã tự tăng nên ko cần xét, còn về tên món ăn thì trên thực tế 1 tên món chỉ dùng 1 lần)
    public boolean checkName(){
        String name = txtTenmon.getText();
        SanPham sp = spd.selectByName(name);
        if(sp == null)
            return true;
        else{
            MsgBox.alert(this, "Tên món ăn này đã xuất hiện trong menu");
            return false;
        }           
    }
    
    //thêm mới
    public void addSP(){
        if(this.checkValidate()){
            if(this.checkName()){
                try{
                    SanPham sp = this.getForm();
                    spd.insert(sp);
                    fillMenu();
                    MsgBox.alert(this, "Thêm mới thành công");
                    this.clearForm();
                    updateStatus();
                }
                catch(Exception e){
                    MsgBox.alert(this, "Thêm thất bại");
                    throw new RuntimeException(e);
                }
            }
        }
    }
    
    //chỉnh sửa sản phẩm
    public void editSP(){
        if(this.checkValidate()){
            if(txtTenmon.getText().toString().equalsIgnoreCase(Auth.sanpham.getTenSp()) || this.checkName()){
                try{
                    SanPham sp = this.getForm();
                    sp.setMaSp(Auth.sanpham.getMaSp());
                    spd.update(sp);
                    Auth.sanpham = sp;
                    fillMenu();
                    MsgBox.alert(this, "Cập nhật thành công");
                    updateStatus();
                }
                catch(Exception e){
                    MsgBox.alert(this, "Edit thất bại");
                    throw new RuntimeException(e);
                }
            }
        }
    }
    
    //xóa sp
    public void deleteSP(){
        if(MsgBox.confirm(this, "Bạn muốn xóa Sản phẩm : "+Auth.sanpham.getTenSp())){
            try{
                spd.delete(String.valueOf(Auth.sanpham.getMaSp()));
                clearForm();
                fillMenu();
                MsgBox.alert(this, "Đã xóa");
                updateStatus();
            }
            catch(Exception e){
                MsgBox.alert(this, "Xóa thất bại");
                throw new RuntimeException(e);
            }
        }
    }
    
    
