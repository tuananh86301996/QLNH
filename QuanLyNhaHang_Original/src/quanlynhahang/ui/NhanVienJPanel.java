    //check validate
    public boolean checkValidate(){
        String patt = "\\w+@\\w+(\\.\\w+){1,2}";
        Pattern pattern = Pattern.compile(patt);
        Matcher matcherMail = pattern.matcher(txtEmail.getText());
        
        String pattPhone = "0+([0-9]{9})";
        Pattern patternP = Pattern.compile(pattPhone);
        Matcher matcherPhone = patternP.matcher(txtSodt.getText());
        
        int year1 = dchNgaySinh.getDate().getYear();
        int year2 = new Date().getYear();
        
        if(txtMaNv.getText().length()==0){
            MsgBox.alert(this,"Vui lòng không để trống Mã" );
            txtMaNv.requestFocus();
            return false;
        }
        else if(txtTen.getText().length()==0){
            MsgBox.alert(this,"Vui lòng không để trống Tên" );
            txtTen.requestFocus();
            return false;
        }
        else if(txtMatKhau.getText().length()==0){
            MsgBox.alert(this,"Vui lòng không để trống Mật Khẩu" );
            txtMatKhau.requestFocus();
            return false;
        }
        else if(matcherPhone.matches()==false){
            MsgBox.alert(this,"Số đt không đúng chuẩn" );
            txtSodt.requestFocus();
            return false;
        }
        else if(matcherMail.matches()==false){
            MsgBox.alert(this,"Email không đúng chuẩn" );
            txtEmail.requestFocus();
            return false;
        }
        else if((year2 - year1) < 18){
            MsgBox.alert(this,"Tuổi phải lớn hơn 18" );
            dchNgaySinh.requestFocus();
            return false;
        }
        else
            return true;       
    }
    
