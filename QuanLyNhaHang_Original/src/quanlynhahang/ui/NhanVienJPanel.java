    public NhanVien getForm(){
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNv.getText());
        nv.setTenNV(txtTen.getText());
        nv.setMatkhau(txtMatKhau.getText());
        
        if(rdoNhanvien.isSelected())
            nv.setVaiTro(false);
        else
            nv.setVaiTro(true);
        
        if(cboCa.getSelectedItem().equals("Sáng"))
            nv.setCa("Sáng");
        else if(cboCa.getSelectedItem().equals("Chiều"))
            nv.setCa("Chiều");
        else nv.setCa("Tối");
        
        if(rdoNam.isSelected())
            nv.setGioiTinh(true);
        else
            nv.setGioiTinh(false);
        
        nv.setSoDT(txtSodt.getText());
        nv.setEmail(txtEmail.getText());
        nv.setNgaySinh(new java.sql.Date(dchNgaySinh.getDate().getTime()));
        nv.setNgayLam(new java.sql.Date(dchNgayLam.getDate().getTime()));
        nv.setDiaChi(txtDiachi.getText());
        nv.setAvata(lblAvata.getToolTipText());
        return nv;
    }
    
